// replaceAll():
// This method is used to replace all occurrences of a specified substring with a new string in a given string. 

const str = "Hello World";
const replacedStr = str.replaceAll("Hello", "Hi");
console.log(replacedStr); // Output: Hi World

const string = "W E L C O M E";
const removedSpace = string.replaceAll(" ", "");
console.log(removedSpace); // output: WELCOME

