// concat(string1, string2, ...): 
// This method concatenates two or more strings and return a new string.

const string1 = "Hii";
const string2 = "Hello";
const string3 = "World";
const greeting = string1.concat(", ", string2, ", ", string3);
console.log(greeting);   // Output: "Hii, Hello, World"

