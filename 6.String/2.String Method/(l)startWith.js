// startsWith(searchValue, startIndex):
// This method check if a string start with a specific searchValue.
// The optional parameter startIndex and length allow you to define a specific portion of the string to check.

const str = "Hellos, World";
console.log(str.startsWith("h")); // Output: false
console.log(str.startsWith("World", 8)); // Output: true (Start checking from index number 8)

