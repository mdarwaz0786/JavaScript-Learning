// charCodeAt(index):
// Returns the Unicode value of the character at the specified index in the string.
// If the index is out of range, NaN is returned.

const str = "Hello";
console.log(str.charCodeAt(0)); // Output: 72 (Unicode value of "H")
console.log(str.charCodeAt(1)); // Output: 101 (Unicode value of "e")

