// Variable: 
// var, let and const.
// Variable are used to store data.
// Variable is a name of a storage location.

// Rule for choosing variable name:
// 1.Letter, digit, underscore and $ sign allowed.
// 2.Must begin with letter or $ or underscore.
// 3.Not begin with number.
// 4.Javascript reserved keyword cannot be used as a variable name.
// 5.Variable is case-sensitive. Arwaz and arwaz are different variable.

// var is a function scope while let and const are block scope.
// var can be updated and re-declared.
// let can be updated but not re-declared.
// const can neither be updated nor be re-declared.
// const must be initialized during declaration but not let and var.
// before executing code var variable is initialized with undefined whereas let and const variable are not initialized.

// var:
var myFirstName = "Arwaz";
console.log(myFirstName); // output: Arwaz

// let:
let myAge = 18;
console.log(myAge); // output: 18

// const:
const pi = 3.14;
console.log(pi); // output: 3.14

// Finished.
