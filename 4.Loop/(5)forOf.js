// for Of loop:
// The for of loop is used to iterate over the value of an iterable (array, string, set, map).

const array = [10, 20, 30, 40, 50];
for (let value of array) {
    console.log(value);
};

// output:
// 10
// 20
// 30
// 40
// 50

const string = "ARWAZ";
for (let value of string) {
    console.log(value);
};

// output:
// A
// R
// W
// A
// Z

// Finished.

