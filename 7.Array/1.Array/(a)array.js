// Array:
// an array is a data structure that allows you to store multiple values in a single variable.

// Create an  Array:
// You can create an array using array literals [], which is a list of comma separated value enclosed in square bracket.
const fruits = ['apple', 'orange', 'banana', 'grape'];

// you can create an array using Array constructor:
const numbers = new Array(1, 2, 3, 4, 5);

// Accessing element in an Array:
// You can access element in the array using their index.
// The index start from 0 for the first element, 1 for the second element, and so on.

console.log(fruits[0]); // Output: apple
console.log(fruits[2]); // Output: banana

