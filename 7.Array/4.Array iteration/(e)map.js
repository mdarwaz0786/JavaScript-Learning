// map():
// Creates a new array by applying a function to each element in the array.

const fruits = ['apple', 'peach', 'cherry', 'grape']
const uppercasedFruits = fruits.map((fruit) => fruit.toUpperCase());
console.log(uppercasedFruits); // Output: ['APPLE', 'PEACH', 'CHERRY', 'GRAPE']

