// splice(): 
// Adds or removes elements from an array. 
// It takes three arguments: the start index, the number of elements to remove, and optional elements to add.

const fruits = ['apple', 'orange', 'banana', 'grape'];
fruits.splice(0, 1, 'peach', 'cherry'); // Removes 1 element at index 0 and adds 'peach' and 'cherry'
console.log(fruits); // Output: [ 'peach', 'cherry', 'orange', 'banana', 'grape' ]

