// shift():
// Removes the first element from the array and returns that element.

const fruits = ['kiwi', 'apple', 'orange', 'banana', 'grape']
const removedElement = fruits.shift();
console.log(removedElement); // Output: 'kiwi'
console.log(fruits); // Output: ['apple', 'orange', 'banana', 'grape']

