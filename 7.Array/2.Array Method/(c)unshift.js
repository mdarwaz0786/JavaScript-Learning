// unshift():
// Adds one or more elements to the beginning of the array and returns the new length of the array.

const fruits = ['apple', 'orange', 'banana', 'grape', 'pear'];
fruits.unshift('kiwi');
console.log(fruits); // Output: ['kiwi', 'apple', 'orange', 'banana', 'grape', 'pear']

