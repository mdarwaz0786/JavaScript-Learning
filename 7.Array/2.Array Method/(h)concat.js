// concat():
// Combines two or more arrays, creating a new array without modifying the existing ones.

const array1 = [1, 2, 3];
const array2 = [4, 5, 6];
const combinedArray = array1.concat(array2); // Combines array1 and array2
console.log(combinedArray); // Output: [1, 2, 3, 4, 5, 6]

