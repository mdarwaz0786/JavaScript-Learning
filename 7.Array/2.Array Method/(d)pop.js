// pop():
// Removes the last element from the array and returns that element.

const fruits = ['kiwi', 'apple', 'orange', 'banana', 'grape', 'pear'];
const removedElement = fruits.pop();
console.log(removedElement); // Output: 'pear'
console.log(fruits); // Output: ['kiwi', 'apple', 'orange', 'banana', 'grape']

