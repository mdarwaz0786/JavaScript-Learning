// Math object:
// The javaScript Math object allows you to perform mathematical tasks on numbers.
console.log(Math.PI);

// Math.round():
// returns the value of x rounded to its nearest integer.
console.log(Math.round(10.65));
console.log(Math.round(10.41));

// Math.pow():
// it returns the value of x to the power of y.
console.log(Math.pow(2, 3));

// Math.sqrt():
// it returns the square root of x.
console.log(Math.sqrt(25));

// Math.abs():
// it return the absolute(positive) value of x.
console.log(Math.abs(-55.6));
console.log(Math.abs(4 - 6));

// math.ceil():
// it returns the value of x rounded up to its nearest integer.
console.log(Math.ceil(4.2));
console.log(Math.ceil(4.7));

// Math.floor():
// it returns the value of x rounded down to its nearest integer.
console.log(Math.floor(4.7));
console.log(Math.floor(99.1));

// Math.main():
// it can be used to find the lowest value in a list of arguments.
console.log(Math.min(0, 150, 30, 20, -8, -200));

// Math.max():
// it can be used to find the highest value in a list of arguments.
console.log(Math.max(0, 150, 30, 20, -8, -200));

// Math.random():
// it returns a random number between 0  and 1.
console.log(Math.random()); // random number between 0 to 1.
console.log(Math.floor(Math.random() * 10)); // random number between 0 to 9.
Math.floor(Math.random() * 10) + 1; // random integer between 1 to 10:

// Math.trunc():
// it returns the integer part of a number.
console.log(Math.trunc(4.6));
console.log(Math.trunc(-99.1));

