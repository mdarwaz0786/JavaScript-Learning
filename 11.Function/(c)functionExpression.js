// Function Expression(Anonymous Function):
// Function expression is a way to define a function by assigning it to a variable.
// Anonymous function is a function that does not have a name.
// Anonymous function is also known as a function expression because it is defined using an expression rather than a function declaration.
const addNumbers = function (a, b) {
    return a + b;
};
const result = addNumbers(5, 10);
console.log(result); // Output: 15

