// Promise Definition:
// In JavaScript, a Promise is an object representing the eventual completion or failure of an asynchronous operation and its resulting value.
// Provides better control over asynchronous tasks and simplifies error handling.

// Asynchronous Operations:
// Suitable for tasks that don't provide immediate results.
// Examples include fetching data from servers or reading files.

// States of a Promise:
// Pending: Initial state before completion or failure.
// Resolved: Successful completion of the asynchronous operation.
// Rejected: The operation is failed, providing an error.

// Chaining:
// The .then() method is used with the callback when the promise is successfully resolved.
// The .catch() method is used with the callback when the promise is rejected or if an error occurs.
// The .finally() method gets executed when the promise is either resolved successfully or rejected.