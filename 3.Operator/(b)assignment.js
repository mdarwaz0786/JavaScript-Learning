// Assignment Operators:
// These operators are used to assign values to variables.

// Types of Assignment Operators:
// Assignment (=): Assigns the value of the right operand to the left operand.
// Addition assignment (+=): Adds the right operand to the left operand and assigns the result to the left operand.
// Subtraction assignment (-=): Subtracts the right operand from the left operand and assigns the result to the left operand.
// Multiplication assignment (*=): Multiplies the left operand by the right operand and assigns the result to the left operand.
// Division assignment (/=): Divides the left operand by the right operand and assigns the result to the left operand.
// Modulus assignment (%=): Calculates the modulus of the left operand by the right operand and assigns the result to the left operand.
// Exponentiation (**=):Raises the left operand to the power of the right operand and assigns the result to the left operand.

let a = 10;
let b = 5;
console.log(a += b); // (a = a + b) return: 15
console.log(a -= b); // (a = a - b) return: 10
console.log(a *= b); // (a = a * b) return: 50
console.log(a /= b); // (a = a / b) return: 10
console.log(a %= b); // (a = a % b) return: 0
console.log(a **= b); // (a = a ** b) return: 0

