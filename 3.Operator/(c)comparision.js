// Comparison(Relational) Operators:
// These operators compare two values and return a Boolean value (true or false).

// Types of Comparison Operators:
// Equal to (==): Checks if two values are equal, converting types if necessary.
// Not equal to (!=): Checks if two values are not equal, converting types if necessary.
// Strict equal to (===): Checks if two values are equal without type conversion (checks value and type).
// Strict not equal to (!==): Checks if two values are not equal without type conversion (checks value and type).
// Greater than (>): Checks if the left operand is greater than the right operand.
// Less than (<): Checks if the left operand is less than the right operand.
// Greater than or equal to (>=): Checks if the left operand is greater than or equal to the right operand.
// Less than or equal to (<=): Checks if the left operand is less than or equal to the right operand.

let p = 10;
let q = 5;
console.log(p == q); //return: false
console.log(p != q); //return: true
console.log(p === q); //return: false
console.log(p !== q); //return: true
console.log(p > q); //return: true
console.log(p < q); //return: false
console.log(p >= q); //return: true
console.log(p <= q); //return: false

